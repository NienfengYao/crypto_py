#!/usr/bin/python3
# -*- coding: utf-8 -*-


def ex_sha1():
    import hashlib
    
    " SHA1"
    s = hashlib.sha1()
    '''
    0xda, 0x39, 0xa3, 0xee, 0x5e, 0x6b, 0x4b, 0x0d, 0x32, 0x55, 0xbf, 0xef,
    0x95, 0x60, 0x18, 0x90, 0xaf, 0xd8, 0x07, 0x09
    '''
    data = ""
    s.update(data.encode("utf-8"))
    print("sha1: ", s.hexdigest())

    " SHA224 Single"
    data = bytes([0x50, 0xef, 0xd0])
    s = hashlib.sha224()
    '''
    0xb5, 0xa9, 0x82, 0x04, 0x13, 0xc2, 0xbf, 0x82, 0x11, 0xfb, 0xbf, 0x5d,
    0xf1, 0x33, 0x70, 0x43, 0xb3, 0x2f, 0xa4, 0xea, 0xfa, 0xf6, 0x1a, 0x0c,
    0x8e, 0x9c, 0xce, 0xde
    '''
    s.update(data)
    print("sha224-single: ", s.hexdigest())

    
    " SHA224 Multipart"
    # data = bytes([0x50, 0xef, 0xd0])
    s = hashlib.sha224()
    s.update(bytes([0x50]))
    s.update(bytes([0xef, 0xd0]))
    print("sha224-multi: ", s.hexdigest())


def main():
    import sys
    print(sys.version)

    ex_sha1()


if __name__ == '__main__':
    main()
